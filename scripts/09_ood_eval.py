#!/usr/bin/env python3
"""
Out-of-Distribution (OOD) Benchmark Sycophancy Evaluation.

Evaluates sycophancy on 3 published Anthropic sycophancy subcategories that
test whether the DPO-trained model's sycophancy reduction generalises to
topic domains not seen during training.

Source: Anthropic/model-written-evals (Perez et al., 2022)
  https://huggingface.co/datasets/Anthropic/model-written-evals

Domains (all OOD-SAME-FORMAT — different topics, same biased/neutral structure):
  1. **NLP Survey**          (nlp_survey.jsonl)
     NLP researcher opinion questions from an NLP community survey.
  2. **Political Typology**  (political_typology.jsonl)
     Political opinion questions from the Pew Political Typology Quiz.

Note: The upstream repo lists a third file (sycophancy_on_philpapers2020.jsonl)
but it is a duplicate of the NLP survey file (identical blob hash), so only two
distinct subcategories are included.

DPO training used the generic "sycophancy" category (seed=100, 400 pairs).
These three subcategories cover distinct topic domains and were never seen
during training, making them true out-of-distribution benchmarks.

Each domain is evaluated on both the pre-DPO baseline and post-DPO model so
the sycophancy reduction can be compared to the in-distribution result.

Prerequisite:
    python scripts/prepare_ood_benchmarks.py  # downloads and adapts data

Usage:
    python scripts/09_ood_eval.py
    python scripts/09_ood_eval.py --adapter-path results/dpo_model
    python scripts/09_ood_eval.py --skip-baseline  # if baseline already saved

Output:
    results/ood_eval_results.json
"""

