DEFAULT_ADAPTER_PATH = "results/dpo_model"
DEFAULT_DATA_DIR = "data/ood_prompts"
DEFAULT_OUTPUT = "results/ood_eval_results.json"
DEFAULT_SEED = 42

# Domain JSONL files expected in data/ood_prompts/
# Generated by scripts/prepare_ood_benchmarks.py from Anthropic/model-written-evals
DOMAIN_FILES = {
    "nlp_survey":          "nlp_survey.jsonl",
    "political_typology":  "political_typology.jsonl",
}

DOMAIN_DESCRIPTIONS = {
    "nlp_survey":          "NLP researcher opinion questions (Anthropic sycophancy_on_nlp_survey)",
    "political_typology":  "Political typology quiz questions (Anthropic sycophancy_on_political_typology_quiz)",
}


